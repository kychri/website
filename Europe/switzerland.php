<!-- Meet and Greet  -->
We have been rolling deep in the Eurail today. Four different trains from 8am to 5pm
with a bus ride at the end. I love travel days so re-traveling through France and
up into Switzerland is a relaxing day for me to catch up on sleep and reflect on
everything that has happened. Once we caught the right bus to our host's apartment,
Alex and Kara, it was easy-going from there. Since the twins had not seen these family members in 14 years, neither
of us had expectations on what to expect. They greeted us with cold beers and a vegetable
infused dinner. Switzerland 1 : USA 0. Their apartment is incredibly hipster with plant lining shelves
and vines climbing their walls. Bookshelves were filled with books on international travel
and included kombucha brewing containers on top. Alex and Kara recently took a
6 month trip and hiked the entire Appalachian Trail in the US. They were
extremely personable and we were soon discussing our Europe trip and what we
wanted to do with our two full days in Switzerland. Kara was studying for final
exams at her university, but luckily Alex had taken time off and had the time to
us around the town of Lausanne. We stayed in that night, playing card games and drinking
beers while talking over potential activities. By the end of the night, we had settled
on doing a short hike to the top of town, getting lunch at the town market, and
then taking a long walk across Lausanne to tour the Chillion Castle. Their apartment
is extremely small, so two of us are sleeping in the living room and the other two
in a tent outside. The price of living in a beautiful country like Switzerland.
<!-- Exploring  -->
We woke up late in the morning to coffees in hand from Kara before she went to the
library to study. Her morning energy is contagious and full of wonderlust. It was about 45 degrees outside so the coffee was
served a dual purpose for the cold and wakeup. We got out the door and hiked up to a wooden Swiss
tower to help establish our sense of direction around Lausanne. It had a magnificent overlook of the
town and and provided an observation platform for viewing the old architecture of Lausanne. We visited the Cathedral of Notre Dame before heading
to the town market for lunch. The Cathedral was unde renovation but displayed the most beautiful stained glass
that I had ever seen. The stained glass included every color shade within the panels. After lunch we visited Kara at the library and she
guided us through a geological musuem displaying dinosaurs and fossils. Then
we made plans for lunch and walked to Aldi's to buy a feast
for all of us. I got a pizza baguette, fruit tart baguette, chocolate croissant, bread roll,
cheese spread, and six chicken sausages. IT WAS INCREDIBLE AND CHEAP. Cost 9 Swiss Francs
which is about $8.50 Amercian dollars. After finishing lunch we began the long walk along
Lake Geneve ~2 miles until we reached the Chillon castle.

<!-- Hiking  -->
Woke up to a beautiful blue sky day outside in Switzerland this morning. We started the day
off with a cup of coffee and tiny, handmade bacon croissants. Each of us made sandwhiches to take
for the hike and then we set out for a full day of outdoor activities. Alex was our personal
travel guide for the day and drove us up to the top of mountain where we started our hike.
The plan was to hike a few miles up to the ridgeline and then follow that to the mountain summit.
From there we would begin the descent downards and splunk through a cave in order to finish the full descent.
Alex has traversed the entire Appalachian Trail and performed many one-onth hiking stints through various European
countries. He said the hike would be challenging so I braced for a long and hilly hike.
Maybe it was the language barrier, but challenging and perilious are not synonyms, at least in the US.
The beginning of the hike was gorgeous. We overlooked Lake Lechon and could also see the Mounatinous Alps
standing behind. I made sure to take a plethora of pictures and videos to capture the view and have a supple
amount of material for the vlog. We made it a little over halfway through the hike and it became more of a mountaineering
adventure then a hike. Making our way through the snow capped portions of the Alps,
footing became my top priority and I began digging my fingers into the several feet of
snow to gain an extra point of contact. We had traveled across about 1/4 mile of snow at a rooftop equivalent
angle before reaching another mountain summit. From there, we could see the mountaineering path that lay ahead of us.
We had another 2.5 hours of snow capped mountaineering that included scaling mountain cliffs and building our own footing and
staircases through the snow to follow the trail. Since we were climing downwards, the footholds would need to support more force,
with one slip resulting in a cliff dive of over 1000ft. We watched a man with climbing gear scale the mountain cliff and
climb across the snow filled face of the mountain before calling of the trip and heading back down the way
that we came. I could tell Alex was dissapointed and wanted to attemp the thirlling climb across the mountain
but neither of us had mountaineering shoes or hiking poles to provide the support we needed to
survive the travel. Anyways, we made our way back down the mountain in about a fourth of the time it took
to hike up. The descent was extremely steep, but not as painful as the hunger that we had
after only eating a sandwhich for the course of the hike. When we arrived back at the start, we stopped by the lodge and I got a bowl
of ice cream while the guys got a round of beers. Less than 5 minutes after finishing the ice cream
I submitted to the fatigue and took a nap on the picnic bench. The view was beautufil from where we were
sitting, close enough to the lake for a camera to capture the beauty.
Next we drove back to their apartment in Lausanne and had a barbque feast including
grilled asparaguas, ribs, cauliflower, garlic bread, and pork steaks. It was a ton of food and
but a good dent into the hunger we accumlated from the hike.

