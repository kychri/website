<!-- First Day -->
PRAHA AND CZECH GIRLS. TIME TO STEP UP THE GAME.
Arrival from Budapest. We race walked to make the train, D1 athletics at its finest.
Cut a 34 minute walk down to 21 minutes and built up a light sweat going
into the ride. The train had no AC and quickyl became a smokeshow. We sat in a train cabin,
dripping sweat. The train had no AC and took 7 hours. No water, no food. Quite
the travel day. It felt a little better than it should to arrive in Prague.

Got into Prague in the evening. We didnt have time to grab breakfast before the
train ride and by the time we arrived it was 5pm so food was the first thing we
searched out.  We stopped at the firt cheap place that accepted credit card, since we
had no Czech Crowns yet, and it turned out to be a pretty good a baguette chain. Bauguette Boulevard.

After fueling up we chilled in the hostel for a bit. They advertised AC and we were craving it. The
Czech have different understandings of what AC is, like in America AC can keep your place, or I'll comprimise
and say a room cool during the day. The Czech seem to call it a window that keeps it around the temperature of the
outside environmet, or 88 degree city weather. The struggle is real ladies and gentlemen.

We're a group of 4 optimistic guys though so we took ourselves out into the city to find
a bar that served cold beer and AC. Czech AC. So we found a restaurant with outside seating
and cheap air that served a classier dinner. The place was a retired airplane hanger and
had a healthy / fancy dinner to start Prague. Their menu was fantastic and I remembered how
well the German dumplings with meat was so I ordered a similiar dish. Delicious, not quite
up to German standards but well worth the price.

Afterwards we walked the city with a goal of finding a cool bar that was showing
the Budapest vs Wales soccer game. I mean we were in Europe for their national championship game
and the European cup so soccer became a theme for weekday bars.
Jack and Aaron and I got a beer at the the Dubliner bar. Feeling a little sick so it was a 1 beer night
for me and I went back around 11:30pm. Early night.

<!-- Second Day -->
Woke up feeling a little better and grabbed an early shower and solo hostel breakfast.
We finally have a hostel that serves a real breakfast, like the ones before would
have read, salami, yogurt, and butter. Some well spent euros for below-average lunch
food. Not in Prague. We had a chef making pancakes and waffles to order and then sunny-side up
eggs in the masses with fruit, cereal, yogurt, and bread. Delicious, costs more than normal but
still worth it for a $6 breakfast. It would cost way more than that at a restaurant. Picture Golden
Corral but for breakfast food. Yessir.

We went straight from breakfast to the Old Town Walking Tour and got to know the history behind
Old Town Prague.

Beer Musuem

Walked the city, Charles Bridge, Prague Castle, Park, Olympic Stadium, Tower
Stopped for a beer and got caught in the rain on the way back.

Chilled at the hostel for a bit. Rallied and then did a pub crawl tour starting in
old town square.

<!-- Third Day -->
Sick, hungover, all of the above.Struggled to rally and do anything today. Decided to bike around
Prague after eating a Chimi Con. Bad decision #1. Had a great time biking and getting fresh air in the
park. Super dehydrated and didnt eat anything real until dinner, then had 2 street food Kebabs.

Got back to hostel and slept for 3 hours. Walked around and got a baguette before bed. Fell like
I am concussed. Couldnt fall asleep from the nap so researched Berlin clubbing and
what it takes to get into a premiere club, outfits, look, etc...

<!-- Departure Day -->
Feel a little bit better but still sick. AS if its a light virus.

